        private void Print_Execute(object sender, SimpleActionExecuteEventArgs e)
        {
            IObjectSpace os = Application.CreateObjectSpace();
            Category cat = (Category)os.GetObject(e.CurrentObject);
            MessageOptions options = new MessageOptions();
            options.Duration = 2000;
            options.Message = string.Format("You have printed the {0} | {1}| {2} Category!", cat.Name, cat.Description,cat.Oid);
            options.Type = InformationType.Success;
            options.Web.Position = InformationPosition.Right;
            options.Win.Caption = "Success";
            options.Win.Type = WinMessageType.Toast;
      
            Application.ShowViewStrategy.ShowMessage(options);
        }
